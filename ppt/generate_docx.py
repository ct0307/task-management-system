add_slide_notes(10, "难点一：状态管理混乱", [
    "第一个比较大的技术难点是状态管理混乱的问题。",
    "项目初期我同时使用了 Jotai 和 Zustand 两个状态管理库——登录状态用 Jotai，任务状态用 Zustand。一开始没觉得有问题，但随着功能增多，两个库之间的状态同步开始出问题。最典型的现象是：页面切换后 Token 状态不同步，偶发 401 未授权错误。还有一个更隐蔽的 Bug：handleFilterChange 调用 setFilters 更新筛选条件后，fetchTasks 因为 JavaScript 闭包的原因，读到的还是更新前的旧 state，导致筛选结果和筛选项对不上。",
    "解决方案分五步走：第一步，全局搜索项目中所有 Jotai 的引用点；第二步，逐一迁移到 Zustand 的 useAuthStore；第三步，删除 package.json 中的 jotai 依赖；第四步，清理兼容导出层；第五步，全局搜索确认零残留。",
    "修复异步闭包 Bug 的关键改动是把 fetchTasks 改为接受显式参数 fetchTasks(newFilters)，不再依赖闭包中的 state 快照。",
    "这个经历让我深刻理解到：技术选型初期就要统一方案，多库并存是最大的技术债。",
])

